let animal = 'cat';

switch(animal) {
    case 'tiger' :
        console.log('호랑이');
        break;
    case 'dog' :
        console.log('개');
        break;
    case 'cat' :
        console.log('고양이');
        break;
    case 'lion' :
        console.log('사자');
        break;
    default :
        console.log('해당 사항 없음');
}